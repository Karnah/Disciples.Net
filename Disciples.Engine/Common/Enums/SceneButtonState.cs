namespace Disciples.Engine.Common.Enums
{
    /// <summary>
    /// Состояние кнопки на сцене.
    /// </summary>
    public enum SceneButtonState
    {
        /// <summary>
        /// Активна.
        /// </summary>
        Active,

        /// <summary>
        /// Невозможно выбрать.
        /// </summary>
        Disabled,

        /// <summary>
        /// Курсор располагается над кнопкой.
        /// </summary>
        Selected,

        /// <summary>
        /// Кнопка нажата.
        /// </summary>
        Pressed
    }
}